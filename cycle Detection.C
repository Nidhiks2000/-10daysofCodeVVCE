bool has_cycle(SinglyLinkedListNode* head) {
 
 // if the list is empty
 if (head == NULL){
        return 0;
    }

// assigning two pointers Slow and fast where fast is 2x fast
    SinglyLinkedListNode*  slow = head;
    SinglyLinkedListNode* fast = head;

//traversing the linked list to find if its circular
    while (fast != NULL && fast->next != NULL){
        slow = slow->next;
        fast = fast->next->next;

        // condition to check if the linked list is circular
        if (slow == fast){  
            return 1;
        }
    }

    return 0;
}
